/*

  Arithmatic Operators

*/

#include <iostream>

using namespace std;

int main()
{

  int numOne = 18;
  int numTwo = 12;

  // Additional Operator

  cout << numOne + numTwo << endl;

  // Substraction Operator

  cout << numOne - numTwo << endl;

  // Multiplication Operator

  cout << numOne * numTwo << endl;

  // Divide Operator

  cout << numOne / numTwo << endl;

  // Modules Operator

  cout << numOne % numTwo << endl;

  return 0;
}